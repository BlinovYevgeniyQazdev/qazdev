#!/usr/bin/python3



import numpy as np

arr = np.array([1, 4, 9, 16])

# Квадратный корень
sqrt_arr = np.sqrt(arr)

# Экспонента
exp_arr = np.exp(arr)

# Натуральный логарифм
log_arr = np.log(arr)

print("Квадратный корень:", sqrt_arr)
print("Экспонента:", exp_arr)
print("Натуральный логарифм:", log_arr)
