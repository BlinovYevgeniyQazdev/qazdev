#!/usr/bin/python3


import numpy as np

arr1 = np.array([1, 2, 3])
arr2 = np.array([4, 5, 6])

# Сложение
sum_arr = arr1 + arr2

# Вычитание
diff_arr = arr1 - arr2

# Умножение
prod_arr = arr1 * arr2

# Деление
div_arr = arr1 / arr2

print("Сложение:", sum_arr)
print("Вычитание:", diff_arr)
print("Умножение:", prod_arr)
print("Деление:", div_arr)
